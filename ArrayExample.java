import java.util.Arrays;

public class ArrayExample {

    public static void main(String[] args) {
        String names[] = new String[6];
        names[0] = "Karan";
        names[1] = "Aakash";
        names[2] = "Sujeet";
        names[3] = "Rishi";
        names[4] = "Raj";
        names[5] = "Rakesh";

        for (int i = 0; i < names.length; i++){
            System.out.println(names[i]);;
        }
    }
}
